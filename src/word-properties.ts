/**
 * Get word break property for a code point
 */
export function propertyWordBreak(codePoint: number): number {
  // Fast path for ASCII
  if (codePoint >= 0x41 && codePoint <= 0x5a) return prALetter; // A-Z
  if (codePoint >= 0x61 && codePoint <= 0x7a) return prALetter; // a-z
  if (codePoint >= 0x30 && codePoint <= 0x39) return prNumeric; // 0-9
  if (codePoint === 0x20) return prWSegSpace; // SPACE
  if (codePoint === 0x2e) return prMidNumLet; // FULL STOP
  if (codePoint === 0x2c) return prMidNum; // COMMA
  if (codePoint === 0x27) return prSingleQuote; // APOSTROPHE
  if (codePoint === 0x22) return prDoubleQuote; // QUOTATION MARK
  if (codePoint === 0x3a) return prMidLetter; // COLON
  if (codePoint === 0x3b) return prMidNum; // SEMICOLON
  if (codePoint === 0x5f) return prExtendNumLet; // LOW LINE
  if (codePoint === 0x0a) return prLF; // LF
  if (codePoint === 0x0d) return prCR; // CR

  // Binary search through the full table
  return binarySearchWordBreakProperty(codePoint);
}

/**
 * Binary search for word break property
 */
function binarySearchWordBreakProperty(codePoint: number): number {
  let left = 0;
  let right = wordBreakCodePoints.length - 1;

  while (left <= right) {
    const mid = Math.floor((left + right) / 2);
    const entry = wordBreakCodePoints[mid];
    if (!entry) {
      break;
    }
    const [start, end, property] = entry;

    if (codePoint >= start && codePoint <= end) {
      return property;
    }

    if (codePoint < start) {
      right = mid - 1;
    } else {
      left = mid + 1;
    }
  }

  // Default: no specific word break property
  return 0; // prAny equivalent
}
